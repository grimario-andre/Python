#Tipo string
n = str(input('Digite um valor'))
print(n)

#tipo inteiro
n1 = int(input('Digite um valor'))
print(n1)

#tipo float
n2 = float(input('Digite um valor real'))
print(n2)

#tipo booleano
v = bool(input('Insira um número'))
print('Quando o tipo e bool a pergunta se {} é verdadeiro ou falto'.format(v))

#Tipo métedo is
b = input(' digite um número')
print(b.isnumeric())
print(b.isalpha())
print(b.isdecimal())